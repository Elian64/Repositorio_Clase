- Tenemos el constructor primario, que es el constructor por defecto o primer constructor que tenemos en nuestra clase para construir objetos. Por otro lado,
contamos con el constructor secundario, el cual se caracteriza por ser otro constructor que nos permite construir objetos, con otras características
(por ejemplo añadir nuevos atributos, o prescindir de otros). Podemos tener un constructor primario y uno o varios constructores secundarios.

3. ¿¡De qué manera afecta y como hay que usar los constructores en la herencia?
 
